package team1444;

import edu.wpi.first.wpilibj.IterativeRobot;

public class RobotMain extends IterativeRobot {

	public RobotMain(){
		super();
	}

	/**
	 * Use it like a constructor. But you can still use a constructor instead of this most of the time
	 */
	@Override
	public void robotInit(){

	}

	/**
	 * This method is always called no matter the mode and it's called after all other periodic methods
	 */
	@Override
	public void robotPeriodic() {
	}

	/**
	 * Called when the robot enters autonomous mode (No human input)
	 * If modes are switched from autonomous to teleop then to autonomous, this will get called again
	 */
	@Override
	public void autonomousInit(){

	}

	/**
	 * Called like a while true loop while in autonomous mode
	 */
	@Override
	public void autonomousPeriodic(){

	}

	/**
	 * Called when the robot enters teleop mode (operator control or human input)
	 */
	@Override
	public void teleopInit() {
	}

	/**
	 * Called like a while true loop while in teleop mode
	 */
	@Override
	public void teleopPeriodic() {
	}

}
